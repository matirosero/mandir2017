function mandir_replace_dashes($str) {
    $str = str_replace(" ", "-", $str);
    return $str;
}

function mandir_convert_to_lowercase_dash($str) {
	$str = trim(mandir_remove_accents($str));
	$str = mandir_replace_dashes($str);
	$str = mb_strtolower($str, 'UTF-8');
	return $str;
}

function mandir_is_tree($pid) {      // $pid = The ID of the page we're looking for pages underneath
	global $post;         // load details about this page
	if(is_page()&&($post->post_parent==$pid||is_page($pid))) 
               return true;   // we're at the page or at a sub page
	else 
               return false;  // we're elsewhere
};


// Replaces the excerpt "Read More" text by a link
function mandir_excerpt_more($more) {
    global $post;
    if ( is_english() ) {
    	$text = 'Read more';
    } else {
    	$text = __('Read more','mandir');
    }
	return '[...] <a class="moretag" href="'. get_permalink($post->ID) . '">'.$text.'<i class="icon-right-open-small"></i></a>';
}
add_filter('excerpt_more', 'mandir_excerpt_more');